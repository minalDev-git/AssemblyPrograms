DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
;INITIALIZING AN ARRAY
ARR DB 'A','B','C'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
;ADDRESS OF THE STARTING OF ARRAY IS MOVED IN SI
MOV SI,OFFSET ARR
MOV CX,3
;LOOP TO PRINT THE VALUES OF THE ARRAY
L1:
MOV DX,[SI]
MOV AH,2
INT 21H
INC SI
LOOP L1
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN

