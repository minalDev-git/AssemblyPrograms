DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC

	MOV AH,00H ;SET VIDEO MODE
	MOV AL,0DH ;resolution 320 x 200 graphics
	INT 10H ; SETTING INTERRUPT FOR GRAPHICS VIDEO MODE
	
	;ACESSING AND DISPLAYING A PIXEL
	MOV AH, 0CH;
	MOV AL, 04H ; COLOR: RED
	MOV CX,50 ; COLUMN # 0 TO 299
	MOV DX,50 ; ROW # 0 TO 199
	
	
	;LOOP TO DRWING THE VERTICAL LINE OF RIGHT ANGLED TRIANGLE
	L1:
	INT 10H
	INC DX
	CMP DX,100
	JE 	L2
	JMP L1
	
	;LOOP TO DRWING THE HORIZONTAL LINE OF RIGHT ANGLED TRIANGLE
	L2:
	INT 10H
	INC CX
	CMP CX,100
	JE 	L3
	JMP L2
	
	;LOOP TO DRWING THE SLANTED LINE OF RIGHT ANGLED TRIANGLE
	L3:
	INT 10H
	DEC CX
	DEC DX
	CMP DX,50
	JE 	SECOND_TRI
	JMP L3
	
	SECOND_TRI:
	MOV CX,100 ; COLUMN # 0 TO 299
	MOV DX,100 ; ROW # 0 TO 199
	
	;LOOP TO DRWING THE VERTICAL LINE OF RIGHT ANGLED TRIANGLE
	L5:
	INT 10H
	INC DX
	CMP DX,150
	JE 	L6
	JMP L5
	
	;LOOP TO DRWING THE HORIZONTAL LINE OF RIGHT ANGLED TRIANGLE
	L6:
	INT 10H
	INC CX
	CMP CX,150
	JE 	L7
	JMP L6
	
	;LOOP TO DRWING THE SLANTED LINE OF RIGHT ANGLED TRIANGLE
	L7:
	INT 10H
	DEC CX
	DEC DX
	CMP DX,100
	JE 	BIG_TRI
	JMP L7
	
	BIG_TRI:
	MOV CX,150 ; COLUMN # 0 TO 299
	MOV DX,150 ; ROW # 0 TO 199
	
	;LOOP TO DRWING THE SLANTED LINE OF BIG TRIANGLE
	L8:
	INT 10H
	DEC DX
	INC CX
	CMP DX,50
	JE 	L9
	JMP L8
	
	;LOOP TO DRWING THE HORIZONTAL LINE OF BIG TRIANGLE
	L9:
	INT 10H
	DEC CX
	CMP CX,50
	JE 	END_PROG
	JMP L9
	
	END_PROG:
	MOV AH,4CH
	INT 21H
	
MAIN ENDP
END MAIN