DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
COUNT DW 0 ;TO COUNT THE LENGTH OF INPUT STRING
ARR DB 100 DUP('$')
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
;ADDRESS OF THE FIRST INDEX OF ARRAY IS STORED
MOV SI,OFFSET ARR
;TAKING STRING AS INPUT
L1:
MOV AH,01
INT 21H
CMP AL,13
JE  PRINT_STR
MOV [SI],AL
INC COUNT
INC SI
JMP L1

;PRINTING THE STRING
PRINT_STR:
MOV DX,OFFSET ARR
MOV AH,09
INT 21H
;enter key CODE
MOV DX,10
MOV AH,02
INT 21H
MOV DX,13
MOV AH,02
INT 21H

CALL REVERSE_STR

MOV AH,4CH
INT 21H
MAIN ENDP
REVERSE_STR PROC
MOV CX,COUNT
MOV SI,OFFSET ARR
;LOOP FOR PUSH AND REVERSING THE STRING
L2:
MOV BX,[SI]
PUSH BX
INC SI
LOOP L2

;LOOP FOR POP AND PRINT
MOV CX,COUNT
L3:
POP DX
MOV AH,02
INT 21H
LOOP L3

RET
REVERSE_STR ENDP
END MAIN