DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB 'NUMBER IS EQUAL$'
MSG2 DB 'NUMBER IS NOT EQUAL$'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
;STORE A VALUE IN A REGISTER TO COMPARE WITH
MOV DL,'3
;TAKING INPUT
MOV AH,1
INT 21H
;COMPARISION
CMP AL,DL
;JUMP TO LABEL L1 IF THEY ARE EQUAL AND EXECUTE THOSE INSTRUCTIONS
JE L1
;EXECUTE THESE INSTRUCTIONS IF NUMBERS ARE NOT EQUAL
MOV DX,OFFSET MSG2
MOV AH,9
INT 21H
MOV AH,4CH
INT 21H
L1:
MOV DX,OFFSET MSG1
MOV AH,9
INT 21H
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
