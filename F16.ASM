DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
STRING DB 'ABC'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
;STARTING ADDRESS OF STRING IS STORED IN SI
MOV SI,OFFSET STRING
MOV CX,3
;LOOP TO PUSH STRING CHARACTERS IN THE STACK
L1:
MOV BX,[SI]
PUSH BX
INC SI
LOOP L1
;LOOP FOR POP AND PRINT
MOV CX,3
L2:
POP DX
MOV AH,2
INT 21H
LOOP L2
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN

