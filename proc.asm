DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
STR1 DB 'KARACHI$'
STR2 DB 'LAHORE$'
STR3 DB 'ISLAMABAD$'
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
;PRINTING THE 1ST STRING
MOV DX,OFFSET STR1
MOV AH,09
INT 21H
;CALLING THE ENTERKEY PROCEDURE
CALL ENTERKEY

;PRINTING THE 2ND STRING
MOV DX,OFFSET STR2
MOV AH,09
INT 21H

CALL ENTERKEY

;PRINTING THE 3RD STRING
MOV DX,OFFSET STR3
MOV AH,09
INT 21H

MOV AH,4CH
INT 21H
MAIN ENDP
;ENTER-KEY PROCEDURE
ENTERKEY PROC
MOV DX,10
MOV AH,02
INT 21H

MOV DX,13
MOV AH,02
INT 21H

RET
ENTERKEY ENDP
END MAIN