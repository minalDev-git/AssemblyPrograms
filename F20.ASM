DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC
MOV AH,00h
MOV AL,13H
INT 10H
;Drawing a Parallelogram
MOV CX,100
MOV DX,50
MOV SI,50
MOV DI,30

PUSH CX
PUSH DX
PUSH SI
PUSH DI

MOV AH,0CH
MOV AL,00100000B
MOV BH,00H

MOV CX,100
MOV DX,50
SIDE1:
INT 10H
INC CX
INC DX
CMP DX,80
JBE SIDE1

MOV CX,100
MOV DX,50
MOV SI,50
TOP_LINE:
INT 10H
INC CX
DEC SI
JNZ TOP_LINE

MOV CX,120
MOV DX,80
MOV SI,50
BOTTOM_LINE:
INT 10H
INC CX
DEC SI
JNZ BOTTOM_LINE

MOV CX,150
MOV DX,50
SIDE2:
INT 10H
INC DX
DEC CX
CMP DX,80
JBE SIDE2

POP DI
POP SI
POP DX
POP CX

;WAIT FOR KEYPRESS BEFORE EXITING
MOV AH,00H
INT 16H
;RETURN TO TEXT MODE
MOV AH,00H
MOV AL,03H
INT 10H
;EXIT PROGRAM
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN
