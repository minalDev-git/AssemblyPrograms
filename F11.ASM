DOSSEG
.MODEL SMALL
.STACK 100H
.DATA
;iNITIALIZING ARRAY VALUES AS $
ARR DB 100 DUP('$')
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV SI,OFFSET ARR
L1:
MOV AH,1
INT 21H
;KEEP TAKING INPUT UNTIL ENTER KEY IS PRESSED
CMP AL,13
JE PRINTSTRING
;INPUT IS STORED IN THE ARRAY
MOV [SI],AL
INC SI
JMP L1
PRINTSTRING:
MOV DX,OFFSET ARR
MOV AH,9
INT 21H
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN

